    public static Date getTanggal(JTable table , int kolom){
        JTable tabeltransaksi = table;
        String tgl = String.valueOf(tabeltransaksi.getValueAt(tabeltransaksi.getSelectedRow(), kolom));
        Date tanggal = null;
        try {
            tanggal = new SimpleDateFormat("yyyy-MM-dd").parse(tgl);
        } catch (ParseException ex) {
            Logger.getLogger(Transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
        return tanggal;
    }
    
    
