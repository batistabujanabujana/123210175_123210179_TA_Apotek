    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:

        String idtransaksi = textidtransaksi.getText();
        String namapemebeli = textnama.getText();
        String idobat = textidobat.getText();
        String namaobat = textnamaobat.getText();
        int harga = Integer.parseInt(textharga.getText());
        int jumlah = Integer.parseInt(textjumlah.getText());
        int total = (harga * jumlah);
        String tgl = String.valueOf(format.format(jtanggal.getDate()));
        try{
            String sql = "INSERT INTO transaksi_detail VALUES ('"+idtransaksi+"','"+namapemebeli+"','"+idobat+"','"+namaobat+"','"+harga+"','"+jumlah+"', '"+total+"','"+tgl+"')";
            PreparedStatement ps = connection().prepareStatement(sql);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan !");
            reset();
            ps.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Data Gagal Disimpan !");
        }
        reset();
        tampilDataTransaksi();
    }//GEN-LAST:event_btnsimpanActionPerformed

